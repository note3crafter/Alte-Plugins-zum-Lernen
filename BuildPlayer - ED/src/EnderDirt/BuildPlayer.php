class BuildPlayer extends PluginBase implements Listener {
	
	public $prefix = Color::WHITE . "[" . Color::YELLOW . "BuildPlayer" . Color::WHITE . "] ";
	
	public function onEnable() {
		
		$this->getServer()->getPluginManager()->registerEvents($this, $this);
        $this->getLogger()->info($this->prefix . Color::GREEN . "wurde aktiviert!");
        $this->getLogger()->info($this->prefix . Color::AQUA . "Made By" . Color::GREEN . " EnderDirt!");
        
    }
    
    public function onCommand(CommandSender $sender, Command $command, string $label, array $args) : bool {
    	
    	if ($command->getName() === "Block") {
    	
    	    if ($sender->isOp()) {
    	
    	        $level = $sender->getLevel();
    	        $x = $sender->getX();
                $y = $sender->getY();
                $z = $sender->getZ();
                $sender->getLevel()->setBlock(new Vector3($x,($y),$z), Block::get(Block::DIAMOND_BLOCK));
                
            }
            
        }
        
    	return true;
    
    }
	
}